package com.orchids.mybatis;

import com.orchids.mybatis.mapper.DeptMapper;
import com.orchids.mybatis.mapper.EmpMapper;
import com.orchids.mybatis.pojo.Dept;
import com.orchids.mybatis.pojo.Emp;
import com.orchids.mybatis.utils.SqlSessionUtil;
